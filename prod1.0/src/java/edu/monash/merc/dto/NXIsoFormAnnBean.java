package edu.monash.merc.dto;

import java.io.Serializable;

/**
 * NXIsoFormAnnBean class which is a DTO class populates the NXIsoFormAnnBean object.
 *
 * @author Simon Yu
 *         <p/>
 *         Email: xiaoming.yu@monash.edu
 * @version 1.0
 * @since 1.0
 *        <p/>
 *        Date: 10/05/12 1:02 PM
 */
public class NXIsoFormAnnBean implements Serializable {
    private String isoFormRef;

    private int firstPosition;

    private String firstStatus;

    private int lastPosition;

    private String lastStatus;

    public String getIsoFormRef() {
        return isoFormRef;
    }

    public void setIsoFormRef(String isoFormRef) {
        this.isoFormRef = isoFormRef;
    }

    public int getFirstPosition() {
        return firstPosition;
    }

    public void setFirstPosition(int firstPosition) {
        this.firstPosition = firstPosition;
    }

    public String getFirstStatus() {
        return firstStatus;
    }

    public void setFirstStatus(String firstStatus) {
        this.firstStatus = firstStatus;
    }

    public int getLastPosition() {
        return lastPosition;
    }

    public void setLastPosition(int lastPosition) {
        this.lastPosition = lastPosition;
    }

    public String getLastStatus() {
        return lastStatus;
    }

    public void setLastStatus(String lastStatus) {
        this.lastStatus = lastStatus;
    }
}
